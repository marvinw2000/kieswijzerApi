class LoginFormAutenticatorAuthenticator extends AbstractGuardAuthenticator
{

    private UserRepository $userRepository;

    public function __construct(UserRepository $userRepository, RouterInterface $router)
    {
        $this->userRepository = $userRepository;
        $this->router = $router;
    }

    public function supports(Request $request): ?bool
    {
        return ($request->getPathInfo() === '/login' && $request->isMethod('POST'));
    }

    public function authenticate(Request $request): PassportInterface\
    {
        $gebruikersnaam = $request->request->get('gebruikersNaam');
        $wachtwoord = $request->request->get('wachtwoord');
        return new Passport(
            new UserBadge($gebruikersnaam, function ($userIdentifier){
                // optionally pass a callback to load the User manually
                $user = $this->userRepository->findOneBy(['email' => $userIdentifier]);
                if (!$user) {
                    throw new UserNotFoundException();
                }
                return $user;
            }),
            new CustomCredentials(function($credentials, User $user) {
                return $credentials === 'tada';
            }, $wachtwoord)
        );
    }

